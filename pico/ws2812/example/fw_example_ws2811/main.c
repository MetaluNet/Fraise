#define BOARD pico

#include <stdio.h>
#include <stdlib.h>

#include "pico/stdlib.h"
#include "hardware/pio.h"
#include "hardware/clocks.h"
#include "ws2812.h"

#define IS_RGBW true
#define NUM_PIXELS 150

#define put_pixel ws2812_put_pixel

void pattern_snakes(uint len, uint t) {
    for (uint i = 0; i < len; ++i) {
        uint x = (i + (t >> 1)) % 64;
        if (x < 10)
            put_pixel(urgb_u32(0xff, 0, 0));
        else if (x >= 15 && x < 25)
            put_pixel(urgb_u32(0, 0xff, 0));
        else if (x >= 30 && x < 40)
            put_pixel(urgb_u32(0, 0, 0xff));
        else
            put_pixel(0);
    }
}

void pattern_random(uint len, uint t) {
    if (t % 8)
        return;
    for (int i = 0; i < len; ++i)
        put_pixel(rand());
}

void pattern_sparkle(uint len, uint t) {
    if (t % 8)
        return;
    for (int i = 0; i < len; ++i)
        put_pixel(rand() % 16 ? 0 : 0xffffffff);
}

void pattern_greys(uint len, uint t) {
    int max = 100; // let's not draw too much current!
    t %= max;
    for (int i = 0; i < len; ++i) {
        put_pixel(t * 0x10101);
        if (++t >= max) t = 0;
    }
}

typedef void (*pattern)(uint len, uint t);
const struct {
    pattern pat;
    const char *name;
} pattern_table[] = {
        {pattern_snakes,  "Snakes!"},
        {pattern_random,  "Random data"},
        {pattern_sparkle, "Sparkles"},
        {pattern_greys,   "Greys"},
};

//-------------------------------------------//

repeating_timer_t tick_timer;

int pat;
int dir;
int t = 0;
int i = 1000;

bool tick_callback(repeating_timer_t *rt)
{
	if(i++ >= 1000) {
        pat = rand() % count_of(pattern_table);
        dir = (rand() >> 30) & 1 ? 1 : -1;
        i = 0;
	}
	pattern_table[pat].pat(NUM_PIXELS, t);
	t += dir;
	return true;
}

void setup() {
    ws2812_setup(IS_RGBW);
	add_repeating_timer_ms(10, tick_callback, NULL, &tick_timer);
}

